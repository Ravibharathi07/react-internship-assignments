"use strict"

const checkNumber = function(x,y){
    if(x===50||y===50){
        return true
    } else if (x+y===50){
        return true
    } else {
        return false
    }
}
console.log(checkNumber(45,5))
console.log(checkNumber(35,5))
console.log(checkNumber(50,35))
console.log(checkNumber(35,15))


 





   
